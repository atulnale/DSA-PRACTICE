package problem36;

import org.junit.jupiter.api.Test;

import static org.junit.jupiter.api.Assertions.*;

class Approach1Test {
    Approach1 approach1 = new Approach1();

    @Test
    void basic1() {
        char[][] sudoku = {
                {'5', '3', '.',   '.', '7', '.',   '.', '.', '.'},
                {'6', '.', '.',   '1', '9', '5',   '.', '.', '.'},
                {'.', '9', '8',   '.', '.', '.',   '.', '6', '.'},
                {'8', '.', '.',   '.', '6', '.',   '.', '.', '3'},
                {'4', '.', '.',   '8', '.', '3',   '.', '.', '1'},
                {'7', '.', '.',   '.', '2', '.',   '.', '.', '6'},
                {'.', '6', '.',   '.', '.', '.',   '2', '8', '.'},
                {'.', '.', '.',   '4', '1', '9',   '.', '.', '5'},
                {'.', '.', '.',   '.', '8', '.',   '.', '7', '9'}
        };
        boolean validSudoku = approach1.isValidSudoku(sudoku);
        assertTrue(validSudoku);
    }
    @Test
    void basic2() {
        char[][] sudoku = {
                {'8', '3', '.', '.', '7', '.', '.', '.', '.'},
                {'6', '.', '.', '1', '9', '5', '.', '.', '.'},
                {'.', '9', '8', '.', '.', '.', '.', '6', '.'},
                {'8', '.', '.', '.', '6', '.', '.', '.', '3'},
                {'4', '.', '.', '8', '.', '3', '.', '.', '1'},
                {'7', '.', '.', '.', '2', '.', '.', '.', '6'},
                {'.', '6', '.', '.', '.', '.', '2', '8', '.'},
                {'.', '.', '.', '4', '1', '9', '.', '.', '5'},
                {'.', '.', '.', '.', '8', '.', '.', '7', '9'}
        };
        boolean validSudoku = approach1.isValidSudoku(sudoku);
        assertFalse(validSudoku);
    }
}